<div class="side-modal-speaker-about">
    <div class="side-modal-speaker-about-title">
        О спикере
    </div>

    <p>
        Исаак Иосифович&nbsp;с отличием окончил Оренбургский государственный педагогический институт им. В.П. Чкалова по
        специальности «Математика».<br>
    </p>
    <p>
        С 1972 года работал в системе образования в качестве преподавателя математики, завуча и директора школы.&nbsp;
    </p>
    <p>
        В 1989 году перешёл на государственную службу,&nbsp;назначен заведующим отделом образования Промышленного района
        города Оренбурга.
    </p>
    <p>
        С 1996 по 2002 год был руководителем главного управления образования администрации Оренбургской области.
    </p>
    <p>
        В 1998 году прошел обучение в Российской академии государственной службы при Президенте Российской Федерации и
        получил диплом (с отличием) менеджера по специальности «Государственное и муниципальное управление».
    </p>
    <p>
        В 2003 - 2004 годах&nbsp;работал в должности заместителя генерального директора ФГУП Издательство «Просвещение».<br>
    </p>
    <p>
        В 2004 году - приглашен в Министерство образования и науки Российской Федерации на должность директора
        Департамента государственной политики в сфере образования.
    </p>
    <p>
        11 сентября 2007&nbsp;года распоряжением Председателя Правительства Российской Федерации назначен заместителем
        Министра образования и науки Российской Федерации.
    </p>
    <p>
        За время работы в Министерстве образования и науки РФ участвовал в разработке и реализации Приоритетного
        национального проекта «Образование», Комплексного проекта модернизации образования, Федеральных государственных
        образовательных стандартов общего и высшего профессионального образования нового поколения.
    </p>
    <p>
        10 ноября 2010 года Указом Мэра Москвы Исаак Иосифович Калина назначен Министром Правительства Москвы,
        руководителем Департамента образования города Москвы.
    </p></div>

